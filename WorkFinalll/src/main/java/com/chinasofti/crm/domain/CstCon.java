package com.chinasofti.crm.domain;

import com.alibaba.fastjson.annotation.JSONField;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by zxk on 2017/8/9 0009.
 */

//交往记录表
public class CstCon implements Serializable{

    //编号
    private Integer cstConId;

    //客户编号
    private String cstConNo;

    //客户名称
    private String cstConName;

    //交往时间
    private Date cstConTime;

    //交往地点
    private  String cstConAddr;

    //交往概要
    private  String  cstConTitle;

    //交往备注
    private  String cstConMeno;

    //交往详情
    private  String cstConInfo;

    @JSONField(serialize = false)
    private transient Customer customer;


    public CstCon() {
    }

    public Integer getCstConId() {
        return cstConId;
    }

    public void setCstConId(Integer cstConId) {
        this.cstConId = cstConId;
    }

    public String getCstConNo() {
        return cstConNo;
    }

    public void setCstConNo(String cstConNo) {
        this.cstConNo = cstConNo;
    }

    public String getCstConName() {
        return cstConName;
    }

    public void setCstConName(String cstConName) {
        this.cstConName = cstConName;
    }

    public Date getCstConTime() {
        return cstConTime;
    }

    public void setCstConTime(Date cstConTime) {
        this.cstConTime = cstConTime;
    }

    public String getCstConAddr() {
        return cstConAddr;
    }

    public void setCstConAddr(String cstConAddr) {
        this.cstConAddr = cstConAddr;
    }

    public String getCstConTitle() {
        return cstConTitle;
    }

    public void setCstConTitle(String cstConTitle) {
        this.cstConTitle = cstConTitle;
    }

    public String getCstConMeno() {
        return cstConMeno;
    }

    public void setCstConMeno(String cstConMeno) {
        this.cstConMeno = cstConMeno;
    }

    public String getCstConInfo() {
        return cstConInfo;
    }

    public void setCstConInfo(String cstConInfo) {
        this.cstConInfo = cstConInfo;
    }

    public Customer getCustomer() {
        return customer;
    }

    public void setCustomer(Customer customer) {
        this.customer = customer;
    }
}
